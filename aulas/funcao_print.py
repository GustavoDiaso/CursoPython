print("Olá", "Gustavo", sep='_')

# end = caracteres que serão exibidos ao final da execução do print
# certas combinações específicas de caracteres fazem ações específicas a depender do seu S.O
# ex: \n -> combinação de caracteres cuja interpretação resulta numa quebra de linha

print("End pulando uma linha", end='\n')
# por padrão, os caracteres \n serão exibidos após toda execução do comando print

print("Print sem pular linha 1", end='_')
print("Print sem pular linha 2", end='\n')





